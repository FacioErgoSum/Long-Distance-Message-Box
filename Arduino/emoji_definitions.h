// Helper function to draw any 16x16 emoji at specified coordinates
void drawEmoji(Adafruit_ST7735* display, const uint16_t* emoji, int16_t x, int16_t y) {
    for(int16_t j = 0; j < 16; j++) {
        for(int16_t i = 0; i < 16; i++) {
            if(emoji[j * 16 + i] != 0) {  // Only draw non-transparent pixels
                display->drawPixel(x + i, y + j, emoji[j * 16 + i]);
            }
        }
    }
}

